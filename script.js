// Overlay to capture mouse events
const overlay = document.getElementById('overlay');
const bgJs = document.getElementById('bgJs');

// Function to pass events to the particles container
function passEventToParticles(event) {
    const rect = bgJs.getBoundingClientRect();
    const x = event.clientX - rect.left;
    const y = event.clientY - rect.top;
    
    // Create a new event with adjusted coordinates
    const particlesEvent = new MouseEvent(event.type, {
        clientX: x,
        clientY: y,
        bubbles: true,
        cancelable: true,
        view: window
    });
    
    bgJs.dispatchEvent(particlesEvent);
}


overlay.addEventListener('mousemove', passEventToParticles);
overlay.addEventListener('click', passEventToParticles);


